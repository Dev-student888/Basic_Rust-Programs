fn main() {

    /* TIPOS DE OPERADORES ARITMÉTICOS:
       - SOMA => +
       - SUBTRAÇÃO => -
       - DIVISÃO => /
       - MULTIPLICAÇÃO => *
       - RESTO DA DIVISÃO OU MÓDULO => %
    */

    let soma :usize = 5+6;
    let subtracao :isize = 5-6;

    println!("Soma: {} \nSubtração: {}", soma, subtracao);
}
